# Arithmetic Operator

# +, - , * , /

print(2 + 2)  # Addition
print(2 - 2)  # subtraction
print(2 / 2)  # Division
print(2 * 2)  # Multiplication

print(2 ** 2)  # ** - power
print(2 ** 3)  # ** - power
print(2 ** 4)  # ** - power

# // -> Quotient
# % -> Remainder
# / -> Division

print(5 // 2)  # Quotient (int)
print(5 / 2)  # Division
print(5 % 2)  # Modules

# 2 | 5 | 2 - Quotient
#   | 4 |
# --------
#     1 - Remainder
