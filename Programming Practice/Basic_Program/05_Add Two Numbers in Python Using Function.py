# To define a function that take two integers
# and return the sum of those two numbers
def add(a, b):
    return a + b


# initializing the variables
num1 = 12
num2 = 55

# function calling and store the result into sum_of_two_number
sum_of_two_number = add(num1, num2)

# To print the result
print("sum of {0} and {1} is {2};".format(num1, num2, sum_of_two_number))

print("sum {0}".format(num1,num2,sum_of_two_number))